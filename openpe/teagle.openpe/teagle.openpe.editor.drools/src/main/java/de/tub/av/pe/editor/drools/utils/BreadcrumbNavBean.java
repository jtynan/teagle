package de.tub.av.pe.editor.drools.utils;

public class BreadcrumbNavBean {

	private String mainNav;
	
	private String mainNavUrl;
	
	private String nav;
	
	private String navUrl;
	
	private String subnav;
	
	private String subnavUrl;

	public String getMainNav() {
		return mainNav;
	}

	public void setMainNav(String mainNav) {
		this.mainNav = mainNav;
	}

	public String getMainNavUrl() {
		return mainNavUrl;
	}

	public void setMainNavUrl(String mainNavUrl) {
		this.mainNavUrl = mainNavUrl;
	}

	public String getNav() {
		return nav;
	}

	public void setNav(String nav) {
		this.nav = nav;
	}

	public String getNavUrl() {
		return navUrl;
	}

	public void setNavUrl(String navUrl) {
		this.navUrl = navUrl;
	}

	public String getSubnav() {
		return subnav;
	}

	public void setSubnav(String subnav) {
		this.subnav = subnav;
	}

	public String getSubnavUrl() {
		return subnavUrl;
	}

	public void setSubnavUrl(String subnavUrl) {
		this.subnavUrl = subnavUrl;
	}
	
	
}
